package ex05;

public class VarEx02 {
    public static void main(String[] args) {
        int n1 = 10; // 4Byte
        long n2 = 20; // 8Byte
        double n3 = 30.1; // 8Byte
        boolean n4 = true; // or false 1bit
        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n3);
        System.out.println(n4);
    }
}
